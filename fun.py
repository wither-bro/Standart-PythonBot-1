import random

# Список приятных слов для пользователя. 
# Новичок может добавить сюда свои фразы!
COMPLIMENTS = [
    "Ты сегодня выглядишь как настоящий Senior Developer! 😎",
    "Твой код чище, чем мои алгоритмы! ✨",
    "У тебя отличное чувство стиля в программировании! 💻",
    "Не сдавайся, ты на верном пути к созданию своей империи! 🚀",
    "Даже баги боятся твоего интеллекта! 🐛🚫"
]

# Функция, которая выбирает случайную фразу
def get_random_compliment():
    # random.choice — это как вытянуть счастливый билет из пачки
    return random.choice(COMPLIMENTS)

